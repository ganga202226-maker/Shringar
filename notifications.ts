// HELPERS
// ═══════════════════════════════════════════════

let salonIdx = 0;
let artistIdx = 0;
let avatarIdx = 0;
let portfolioIdx = 0;

/** Get a deterministic-ish placeholder salon cover image */
export function getSalonPlaceholder(id?: string): string {
  const index = id ? hashStr(id) % SALON_PLACEHOLDERS.length : salonIdx++ % SALON_PLACEHOLDERS.length;
  return SALON_PLACEHOLDERS[index];
}

/** Get a placeholder artist photo */
export function getArtistPlaceholder(id?: string): string {
  const index = id ? hashStr(id) % ARTIST_PLACEHOLDERS.length : artistIdx++ % ARTIST_PLACEHOLDERS.length;
  return ARTIST_PLACEHOLDERS[index];
}

/** Get a placeholder review avatar */
export function getAvatarPlaceholder(id?: string): string {
  const index = id ? hashStr(id) % AVATAR_PLACEHOLDERS.length : avatarIdx++ % AVATAR_PLACEHOLDERS.length;
  return AVATAR_PLACEHOLDERS[index];
}

/** Get a placeholder portfolio / gallery image */
export function getPortfolioPlaceholder(id?: string): string {
  const index = id ? hashStr(id) % PORTFOLIO_PLACEHOLDERS.length : portfolioIdx++ % PORTFOLIO_PLACEHOLDERS.length;
  return PORTFOLIO_PLACEHOLDERS[index];
}

/** Resolve an image URL — if it's falsy, return a placeholder instead */
export function resolveImage(
  url: string | null | undefined,
  type: 'salon' | 'artist' | 'avatar' | 'portfolio',
  id?: string
): string {
  if (url && url.trim()) return url;
  switch (type) {
    case 'salon':
      return getSalonPlaceholder(id);
    case 'artist':
      return getArtistPlaceholder(id);
    case 'avatar':
      return getAvatarPlaceholder(id);
    case 'portfolio':
      return getPortfolioPlaceholder(id);
    default:
      return SALON_PLACEHOLDERS[0];
  }
}

/** Simple string hash for deterministic index */
function hashStr(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash;
  }
  return Math.abs(hash);
}
